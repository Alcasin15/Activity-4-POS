    void getcash() {
        String val1 = jTextField1.getText();
        String val2 = jLabel2.getText();
        String val3 = jTextField3.getText();
      
        if (val1.equals("")) {
        
            jTextField2.setText("");
        }
        
        else {
    
             Double num1 = Double.parseDouble (val1);
             Double total = Double.parseDouble(val2);
             Double toquan = Double.parseDouble(val3);
             Double overall = total*toquan;
             Double change = num1 - overall;
             jTextField2.setText(change.toString());
           
        }
        
    }
    
