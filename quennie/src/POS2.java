    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
      
        int item = jComboBox1.getSelectedIndex();
        
        if (item == 0) {
            setproduct("apple");
            setprice(100d);
        }
        
        else if (item == 1) {
            setproduct("banana");
            setprice(200d);
        }
        
        else if (item == 2) {
            setproduct("eggplant");
            setprice(300d);
    }
        
        else if (item == 3) {
        
            setproduct ("mango");
            setprice(400d);
        }
        
        else if  (item == 4) {
            setproduct ("pineapple");
            setprice(500d);
        }
        
        else {
            setproduct("");
            setprice(0.00);
        }
        
        jLabel1.setText(jComboBox1.getSelectedItem().toString());
        setprice(getprice());
        jLabel2.setText(getprice().toString());
        
        
    }//GEN-LAST:event_jComboBox1ItemStateChanged

